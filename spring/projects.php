						<h3>3.Supporting senior citizen</h3>
						<p>Besides, Children and women we are also focused on
							providing health related services to elderly citizen of different
							communities. Nepalese society is in a phase of modernization. The
							traditional joint family is slowly being replaced by nuclear
							family in urban areas. The caring of elderly population is a
							major problem. Because of this trend of nuclear family, the older
							members of family are being isolated.</p>
						<p>
							Therefore, many of them are not getting proper care, medical
							facilities and have to stay away from other facilities which they
							deserved. SFN believes, caring of elderly people is a welfare
							scheme that provides meaningful support for the elderly people
							with a view to make their health physically and mentally sound
							and to continue the emotional aspects of their life. Therefore,
							SFN provides the following activities in order to support senior
							citizen of various communities. <br> • Health Camps</br> <br>•
								Medical Checkups</br> <br> • Distributing medicine</br> <br> •
								Organize social and spiritual programs</br>
						</p>
